package com.itcast.PrototypePattern.test1.simpleClone;

public class Attachment {
	private String name;
	
	public Attachment() {
	}
	
	public Attachment(String name) {
		this.name = name;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
}
